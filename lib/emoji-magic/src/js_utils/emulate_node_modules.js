// @fileoverview: Make it so the browser and chrome extension can both run node module syntax.
// The browser works by storing and plucking the require key from the window global
// Node works in the usual way.
// Seemed easier than getting node to support window/global namespaced "modules".
// ...the module wars have to stop, this is a waste of everyone's time.

class EmulatedModule {
  set exports(val) {
    const key = val.__id__; // convention
    // console.log('set exports', val, key);
    if (!key) {
      return console.warn("Can't export a fake browser 'node module' without an __id__");
    }
    window[key] = val;
  }
}

function emulatedRequire(id) {
  let thing;
  id = id.split('/').pop(); // "./store" -> "store"
  // console.log('emulatedRequire', id, thing);
  return window[id];
}



window = this;
window.module = window.module || new EmulatedModule();
window.require = window.require || emulatedRequire;
